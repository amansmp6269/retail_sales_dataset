SELECT COUNT(order_id) AS total_orders
FROM retail_sales;

-- How many unique customers have made purchases?
SELECT COUNT(DISTINCT customer_id) AS total_customers
FROM retail_sales;

-- How many orders were placed in each product category?
SELECT
    product_category,
    COUNT(order_id) AS total_orders
FROM retail_sales
GROUP BY product_category;

-- How many orders were placed by each gender?

SELECT
    gender,
    COUNT(order_id) AS total_orders
FROM retail_sales
GROUP BY gender;

-- What is the total revenue generated by the business?
SELECT SUM(total_revenue) AS total_revenue
FROM retail_sales;

-- What is the total revenue generated by each product category?
SELECT
    product_category,
    SUM(total_revenue) AS total_revenue
FROM retail_sales
GROUP BY product_category;  

-- Which product category sold the highest total quantity of items?
SELECT
    product_category,
    SUM(quantity) AS total_quantity_sold
FROM retail_sales
GROUP BY product_category
ORDER BY total_quantity_sold DESC;

-- What is the average revenue per order for each product category?

SELECT
    product_category,
    AVG(total_revenue) AS average_revenue
FROM retail_sales
GROUP BY product_category;

-- Which customers are the top 5 highest spenders based on total revenue?
SELECT
    customer_id,
    SUM(total_revenue) AS total_spent
FROM retail_sales
GROUP BY customer_id
ORDER BY total_spent DESC
LIMIT 5;

-- Which transactions generated more than $1,000 in revenue?
SELECT
    order_id,
    customer_id,
    product_category,
    quantity,
    total_revenue
FROM retail_sales
WHERE total_revenue > 1000
ORDER BY total_revenue DESC;

-- What is the total profit for each product category?
SELECT
    product_category,
    SUM(total_revenue - cost) AS total_profit
FROM retail_sales
GROUP BY product_category
ORDER BY total_profit DESC;

-- Which product category is the most profitable?
SELECT
    product_category,
    SUM(total_revenue - cost) AS total_profit
FROM retail_sales
GROUP BY product_category
ORDER BY total_profit DESC
LIMIT 1;

-- Which age group contributes the most revenue?

SELECT
    CASE
        WHEN age < 20 THEN 'Under 20'
        WHEN age BETWEEN 20 AND 29 THEN '20-29'
        WHEN age BETWEEN 30 AND 39 THEN '30-39'
        WHEN age BETWEEN 40 AND 49 THEN '40-49'
        ELSE '50+'
    END AS age_group,
    SUM(total_revenue) AS total_revenue
FROM retail_sales
GROUP BY age_group
ORDER BY total_revenue DESC;

-- During which time of the day (Morning, Afternoon, Evening) are the most orders placed?
SELECT
    CASE
        WHEN HOUR(order_time) < 12 THEN 'Morning'
        WHEN HOUR(order_time) BETWEEN 12 AND 17 THEN 'Afternoon'
        ELSE 'Evening'
    END AS shift,
    COUNT(order_id) AS total_orders
FROM retail_sales
GROUP BY shift
ORDER BY total_orders DESC;

-- Which month generated the highest total revenue?

SELECT
    YEAR(order_date) AS year,
    MONTH(order_date) AS month,
    SUM(total_revenue) AS total_revenue
FROM retail_sales
GROUP BY YEAR(order_date), MONTH(order_date)
ORDER BY total_revenue DESC
LIMIT 1;

