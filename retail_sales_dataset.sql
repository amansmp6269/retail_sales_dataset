-- Monthly sales analysis using CTE
WITH monthly_sales AS
(
    SELECT
        YEAR(order_date) AS sales_year,
        MONTH(order_date) AS sales_month,
        SUM(total_revenue) AS monthly_revenue,
        SUM(total_revenue - cost) AS monthly_profit,
        COUNT(order_id) AS total_orders
    FROM retail_sales
    GROUP BY
        YEAR(order_date),
        MONTH(order_date)
)

SELECT *
FROM monthly_sales
ORDER BY sales_year, sales_month;

-- Monthly revenue growth percentage
WITH monthly_sales AS
(
    SELECT
        DATE_FORMAT(order_date, '%Y-%m') AS sales_month,
        SUM(total_revenue) AS monthly_revenue
    FROM retail_sales
    GROUP BY DATE_FORMAT(order_date, '%Y-%m')
),

monthly_comparison AS
(
    SELECT
        sales_month,
        monthly_revenue,
        LAG(monthly_revenue) OVER (
            ORDER BY sales_month
        ) AS previous_month_revenue
    FROM monthly_sales
)

SELECT
    sales_month,
    monthly_revenue,
    previous_month_revenue,
    ROUND(
        (
            monthly_revenue - previous_month_revenue
        ) / NULLIF(previous_month_revenue, 0) * 100,
        2
    ) AS revenue_growth_percentage
FROM monthly_comparison;

-- Top three customers in each category
WITH customer_category_sales AS
(
    SELECT
        product_category,
        customer_id,
        SUM(total_revenue) AS total_spent
    FROM retail_sales
    GROUP BY product_category, customer_id
),

ranked_customers AS
(
    SELECT
        product_category,
        customer_id,
        total_spent,
        DENSE_RANK() OVER (
            PARTITION BY product_category
            ORDER BY total_spent DESC
        ) AS customer_rank
    FROM customer_category_sales
)

SELECT
    product_category,
    customer_id,
    total_spent,
    customer_rank
FROM ranked_customers
WHERE customer_rank <= 3
ORDER BY product_category, customer_rank;

-- Category contribution percentage
SELECT
    product_category,
    SUM(total_revenue) AS category_revenue,

    ROUND(
        SUM(total_revenue) /
        SUM(SUM(total_revenue)) OVER () * 100,
        2
    ) AS revenue_contribution_percentage

FROM retail_sales
GROUP BY product_category
ORDER BY category_revenue DESC;

-- Profit margin by category
SELECT
    product_category,
    SUM(total_revenue) AS total_revenue,
    SUM(cost) AS total_cost,
    SUM(total_revenue - cost) AS total_profit,

    ROUND(
        SUM(total_revenue - cost) /
        NULLIF(SUM(total_revenue), 0) * 100,
        2
    ) AS profit_margin_percentage

FROM retail_sales
GROUP BY product_category
ORDER BY profit_margin_percentage DESC;


